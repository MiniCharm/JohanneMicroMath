using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace JohanneMicroMath
{
    public class JohanneMicroMath
    {
        /// <summary>
        /// Add two numbers
        /// </summary>
        /// <param name="t1">First number</param>
        /// <param name="t2">Second number</param>
        /// <returns>return the result as an int</returns>
        public int Add(int t1, int t2) 
        {
            return t1 + t2;
        }

        /// <summary>
        /// Add three numbers
        /// </summary>
        /// <param name="t1">Fist number</param>
        /// <param name="t2">Second number</param>
        /// <param name="t3">Third number</param>
        /// <returns></returns>
        public int AddThreeNumbers(int t1, int t2, int t3)
        {
            return t1 + t2+t3; //Add
        }

    }
}
